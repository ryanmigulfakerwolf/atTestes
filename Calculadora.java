
package Calculadora;

public class Calculadora {
    
    double v1, v2;
    double result;
    
    public double soma(double result){
        result = v1+v2;
        return result;
    }
    public double subtra(double result){
        result = v1-v2;
        return result;
    }
    public double multipli(double result){
        result = v1*v2;
        return result;
    }
    public double divisao(double result){
        result = v1/v2;
        return result;
    }
}
